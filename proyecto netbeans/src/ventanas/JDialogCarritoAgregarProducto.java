    private void jTableProductosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableProductosMouseClicked
        int fila = jTableProductos.getSelectedRow();
        if (fila >= 0) {
            String costoUnitario = jTableProductos.getModel().getValueAt(fila, 1).toString();
            nombreProducto = jTableProductos.getModel().getValueAt(fila, 0).toString();
            precio = Double.parseDouble(costoUnitario);
            int cantidad = Integer.parseInt(jSpinnerCantidad.getModel().getValue().toString());
            jTextFieldTotal.setText("$ " + (precio * cantidad));
        } else {
            jTextFieldTotal.setText("$ 0.00");
            nombreProducto = "";
            precio = 0;
        }
    }//GEN-LAST:event_jTableProductosMouseClicked

