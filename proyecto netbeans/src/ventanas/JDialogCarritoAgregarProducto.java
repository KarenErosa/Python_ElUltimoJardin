    private void jButtonAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAgregarActionPerformed
        jTableProductosMouseClicked(null);
        int pos = herramientas.ManipulaMesas.posProducto(mesa, nombreProducto);
        int cantidad = Integer.parseInt(jSpinnerCantidad.getModel().getValue().toString());
        if (pos != -1) {
            clases.ProductoVenta productoTmp = mesa.getProductos().get(pos);
            productoTmp.setCantidad(productoTmp.getCantidad() + cantidad);
            JOptionPane.showMessageDialog(this, "El producto ya estaba en la lista, se agregó la cantidad");
        } else if (!nombreProducto.isEmpty()) {
            mesa.getProductos().add(new clases.ProductoVenta(nombreProducto, precio, cantidad));
            JOptionPane.showMessageDialog(this, "El producto se agregó a la lista con éxito");
        } else {
            JOptionPane.showMessageDialog(this, "Favor de tener seleccionado el producto a agregar");
        }
    }//GEN-LAST:event_jButtonAgregarActionPerformed

